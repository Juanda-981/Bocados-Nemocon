let divCount = 0;

function agregarDiv1Cat1() {
    const container = document.getElementById('contentPedid');
    const div = document.createElement('div');
    div.className = 'box';
    divCount++;

    const image = document.createElement('img');
    image.src = imageUrlInput.value; 

    const text = document.createElement('p');
    text.className = 'textDivCatPedid';
    text.textContent = inputNombre1Cat1.value;
    text.id = "textMiniDivCompra1Cat1";

    const deleteButton = document.createElement('button');
    deleteButton.innerHTML = 'Borrar';
    deleteButton.className = 'delete-button-miniDiv';
    deleteButton.addEventListener('click', function() {
        container.removeChild(div);
        divCount--;
    });

    div.appendChild(image);
    div.appendChild(text);
    div.appendChild(deleteButton);
    container.appendChild(div);
    container.scrollTop = container.scrollHeight; // Desplaza el contenedor hacia abajo

    // Agrega un identificador único a cada div
    div.id = 'div-' + divCount;
}
